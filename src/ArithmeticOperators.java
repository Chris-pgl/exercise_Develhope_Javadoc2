public class ArithmeticOperators {
    /**
     * Method summation
     * @param num1
     * @param num2
     * @return  the result sum
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }


    /**
     * Method subtraction
     * @param num1
     * @param num2
     * @return result sub
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }


    /**
     * Method multiplication
     * @param num1
     * @param num2
     * @return result mul
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }


    /**
     * method division
     * @param num1
     * @param num2
     * @return result div
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }


}